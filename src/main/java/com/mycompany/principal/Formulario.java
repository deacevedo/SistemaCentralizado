    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here
        try {
            Productos p = new Productos();
            p.setCodigoProducto(Integer.parseInt(txtCodigo.getText()));
            p.setNombreProducto(txtNombre.getText());
            p.setPrecioProducto(BigDecimal.valueOf(Double.parseDouble(txtPrecio.getText())));
            p.setDetalleProducto(txtDetalles.getText());

            pjc.create(p);
            JOptionPane.showMessageDialog(null, "Los datos han sido guardados con exito!");
            CrearModelo();
            Cargar_Informacion();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } 
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try{
            int codPro = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            pjc.destroy(codPro);
            JOptionPane.showMessageDialog(null, "El registro ha sido eliminado con exito");
            CrearModelo();
            Cargar_Informacion();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

